use std::collections::HashMap;

fn main() {
    part1();
}

const XMAS: [&str; 2] = ["XMAS", "SAMX"];

fn part1() {
    let map: HashMap<_, _> = input()
        .trim()
        .lines()
        .enumerate()
        .flat_map(|(y, line)| {
            line.chars()
                .enumerate()
                .map(move |(x, char)| ((x as i64, y as i64), char))
        })
        .collect();

    let x_max = *map.keys().map(|(x, _)| x).max().unwrap();
    let y_max = *map.keys().map(|(_, y)| y).max().unwrap();

    let mut count = 0;

    {
        for y in 0..=y_max {
            let mut searches = XMAS;

            for x in 0..=x_max {
                search(&map, &mut count, &mut searches, &(x, y));
            }
        }
    }

    {
        for x in 0..=x_max {
            let mut searches = XMAS;

            for y in 0..=y_max {
                search(&map, &mut count, &mut searches, &(x, y));
            }
        }
    }

    {
        for x in -y_max..=x_max {
            let mut searches = XMAS;

            for y in 0..=y_max {
                search(&map, &mut count, &mut searches, &(x + y, y));
            }
        }
    }

    {
        for x in (0..=y_max + x_max).rev() {
            let mut searches = XMAS;

            for y in 0..=y_max {
                search(&map, &mut count, &mut searches, &(x - y, y));
            }
        }
    }

    println!("Part 1 is {count}");
}

fn search(
    map: &HashMap<(i64, i64), char>,
    count: &mut usize,
    searches: &mut [&str; 2],
    coordinates: &(i64, i64),
) {
    let char = *map.get(coordinates).unwrap_or(&'.');

    for (index, search) in searches.iter_mut().enumerate() {
        if char == XMAS[index].chars().nth(0).unwrap() {
            *search = &XMAS[index][1..];
        } else if char == search.chars().nth(0).unwrap() {
            *search = &search[1..];
            if search.is_empty() {
                *count += 1;
                *search = XMAS[index];
            }
        } else {
            *search = XMAS[index];
        }
    }
}

