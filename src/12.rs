use std::collections::{HashMap, HashSet};

fn main() {
    part1();
}

fn part1() {
    let map: HashMap<_, _> = input()
        .trim()
        .lines()
        .enumerate()
        .flat_map(|(y, line)| {
            line.trim()
                .chars()
                .enumerate()
                .map(move |(x, char)| ((x as i64, y as i64), char))
        })
        .collect();

    let mut visited = HashSet::new();
    let mut sum = 0;

    for (coordinates, value) in &map {
        let (fences, area) = visit(&mut visited, &map, *coordinates, *value);
        sum += fences * area;
    }

    println!("Part 1 is {sum}");
}

fn visit(
    visited: &mut HashSet<(i64, i64)>,
    map: &HashMap<(i64, i64), char>,
    position: (i64, i64),
    value: char,
) -> (usize, usize) {
    if visited.contains(&position) {
        return (0, 0);
    }

    visited.insert(position);
    let mut fences = 0;
    let mut area = 1;

    for (x, y) in [(0, -1), (1, 0), (0, 1), (-1, 0)] {
        let new_position = (position.0 + x, position.1 + y);
        if let Some(other) = map.get(&new_position) {
            if *other == value {
                let (new_fences, new_area) = visit(visited, map, new_position, *other);
                fences += new_fences;
                area += new_area;
            } else {
                fences += 1;
            }
        } else {
            fences += 1;
        }
    }

    (fences, area)
}

